let useStyles = makeStyles((props) => ({
  root: {
    width: "100%",
    minWidth: "100px",
    height: "100%",
  },
}))

const MAP_OPTIONS: google.maps.MapOptions = {
  mapTypeControl: false,
  fullscreenControl: false,
  // zoomControl: false,
  panControl: false,
  streetViewControl: false,
  zoom: 3,
  mapId: config.get(GOOGLE_MAPS_ID_HOTEL_PAGE_KEY),
} as google.maps.MapOptions

type AppHotelLocationMapProps = {}

export default function HotelsMap(props: AppHotelLocationMapProps) {
  let classes = useStyles(props)
  let mapRef = useRef<HTMLDivElement | null>(null)
  let [map, setMap] = useState<google.maps.Map | undefined>(undefined)

  let [googleMapScriptLoaded] = useScript(
    `https://maps.googleapis.com/maps/api/js?key=${config.get(
      GOOGLE_API_KEY
    )}&map_ids=${config.get(GOOGLE_MAPS_ID_HOTEL_PAGE_KEY)}&callback`
  )

  useEffect(() => {
    if (googleMapScriptLoaded) {
      if (googleMapScriptLoaded && mapRef.current && !map) {
        let map = new google.maps.Map(mapRef.current, {
          ...MAP_OPTIONS,
          center: {
            lat: 41.850033,
            lng: -87.6500523, // use chicago as center
          },
        })
        setMap(map)
      }
    }
  }, [googleMapScriptLoaded, mapRef, setMap, map])

  useEffect(() => {
    if (googleMapScriptLoaded && mapRef.current && google) {
      let markers = LOCATIONS.map(([id, lat, lng, label]) => {
        let marker = new google.maps.Marker({
          position: {lat, lng},
          label: "",
        })
        let infoWindow = new google.maps.InfoWindow({
          content: label,
          disableAutoPan: true,
        })
        marker.addListener("mouseover", () => {
          infoWindow.open(map, marker)
        })
        marker.addListener("mouseout", () => {
          infoWindow.close()
        })
        marker.addListener("click", () => {
          const newTab = window.open(
            AppRoutes.getPath("HotelPage", {
              hotelId: id.toString(),
            }),
            "_blank"
          )
          newTab?.focus()
        })
        return marker
      })
      new MarkerClusterer({map, markers})
    }
  }, [googleMapScriptLoaded, map])

  return <div ref={mapRef} className={classes.root}></div>
}

